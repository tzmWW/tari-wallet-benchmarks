const WALLET_FINGERPRINT_LABEL: &str = "wallet_identity_fingerprint";

#[derive(Debug, Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum WalletRole {
    OldWallet,
    NewWallet,
    PaymentProcessor,
}

impl WalletRole {
    pub fn label(self) -> &'static str {
        match self {
            Self::OldWallet => "old_wallet",
            Self::NewWallet => "new_wallet",
            Self::PaymentProcessor => "payment_processor",
        }
    }
}

#[derive(Clone, Serialize, Deserialize)]
pub struct SeedMaterial {
    pub role: WalletRole,
    pub env_var: String,
    pub address: String,
    pub birthday: u16,
    #[serde(skip_serializing)]
    pub seed_words: String,
    #[serde(skip_serializing)]
    pub private_view_key_hex: String,
    pub public_spend_key_hex: String,
    pub wallet_fingerprint_hex: String,
}

impl std::fmt::Debug for SeedMaterial {
    fn fmt(&self, formatter: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        formatter
            .debug_struct("SeedMaterial")
            .field("role", &self.role)
            .field("env_var", &self.env_var)
            .field("address", &self.address)
            .field("birthday", &self.birthday)
            .field("public_spend_key_hex", &self.public_spend_key_hex)
            .field("wallet_fingerprint_hex", &self.wallet_fingerprint_hex)
            .finish_non_exhaustive()
    }
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct AddressBook {
    pub generated_at: chrono::DateTime<chrono::Utc>,
    pub addresses: BTreeMap<String, SeedMaterial>,
}

impl AddressBook {
    /// Generates a completely new set of benchmark identities.
    ///
    /// This deliberately ignores configured seed environment variables so that
    /// an exported seed from an earlier run cannot contaminate a fresh namespace.
    pub fn generate_fresh(config: &Config) -> anyhow::Result<Self> {
        let mut addresses = BTreeMap::new();
        for (role, env_var) in seed_envs(&config.seeds) {
            let mut seed = CipherSeed::random();
            if matches!(role, WalletRole::OldWallet | WalletRole::PaymentProcessor) {
                seed.change_birthday(0);
            }
            let material = material_from_seed(role, env_var, seed)?;
            addresses.insert(role.label().to_string(), material);
        }
        Ok(Self {
            generated_at: chrono::Utc::now(),
            addresses,
        })
    }

    /// Loads all configured benchmark identities and fails closed when any seed
    /// environment variable is absent or invalid.
    pub fn load_required(config: &Config) -> anyhow::Result<Self> {
        let mut addresses = BTreeMap::new();
        for (role, env_var) in seed_envs(&config.seeds) {
            let words = env::var(&env_var).with_context(|| {
                format!(
                    "${env_var} must be set; run addresses and source the generated seed env file"
                )
            })?;
            let seed = seed_from_words(&words).with_context(|| format!("parsing ${env_var}"))?;
            let material = material_from_seed(role, env_var, seed)?;
            addresses.insert(role.label().to_string(), material);
        }
        Ok(Self {
            generated_at: chrono::Utc::now(),
            addresses,
        })
    }

    pub fn write_env_file(&self, path: &Path) -> anyhow::Result<()> {
        if let Some(parent) = path.parent() {
            fs::create_dir_all(parent)?;
        }
        let mut file =
            tempfile::NamedTempFile::new_in(path.parent().unwrap_or_else(|| Path::new(".")))?;
        writeln!(file, "# Generated by wallet-bench. Keep this file secret.")?;
        for material in self.addresses.values() {
            writeln!(
                file,
                "export {}='{}'",
                material.env_var, material.seed_words
            )?;
        }
        file.persist(path)?;
        Ok(())
    }

    pub fn public_summary(&self) -> serde_json::Value {
        let entries = self
            .addresses
            .iter()
            .map(|(role, material)| {
                (
                    role.clone(),
                    serde_json::json!({
                        "env_var": material.env_var,
                        "address": material.address,
                        "birthday": material.birthday,
                        "public_spend_key_hex": material.public_spend_key_hex,
                        "wallet_fingerprint_hex": material.wallet_fingerprint_hex,
                    }),
                )
            })
            .collect::<serde_json::Map<_, _>>();
        serde_json::Value::Object(entries)
    }
}

fn seed_envs(seeds: &SeedConfig) -> [(WalletRole, String); 3] {
    [
        (WalletRole::OldWallet, seeds.old_wallet_env.clone()),
        (WalletRole::NewWallet, seeds.new_wallet_env.clone()),
        (
            WalletRole::PaymentProcessor,
            seeds.payment_processor_env.clone(),
        ),
    ]
}

pub fn seed_from_words(words: &str) -> anyhow::Result<CipherSeed> {
    let mnemonic =
        SeedWords::from_str(words).map_err(|e| anyhow::anyhow!("invalid seed words: {e}"))?;
    CipherSeed::from_mnemonic(&mnemonic, None)
        .map_err(|e| anyhow::anyhow!("invalid cipher seed: {e}"))
}

pub fn seed_from_words_with_birthday(words: &str, birthday: u16) -> anyhow::Result<CipherSeed> {
    let mut seed = seed_from_words(words)?;
    seed.change_birthday(birthday);
    Ok(seed)
}

pub fn seed_words_with_birthday(words: &str, birthday: u16) -> anyhow::Result<String> {
    let seed = seed_from_words_with_birthday(words, birthday)?;
    seed_words_from_seed(&seed)
}

pub fn seed_words_from_seed(seed: &CipherSeed) -> anyhow::Result<String> {
    Ok(seed
        .to_mnemonic(MnemonicLanguage::English, None)
        .map_err(|e| anyhow::anyhow!("seed to mnemonic failed: {e}"))?
        .join(" ")
        .reveal()
        .to_string())
}

pub fn current_birthday() -> u16 {
    let birthday_genesis = UNIX_EPOCH + Duration::from_secs(BIRTHDAY_GENESIS_FROM_UNIX_EPOCH);
    let Ok(age) = SystemTime::now().duration_since(birthday_genesis) else {
        return 0;
    };
    u16::try_from(age.as_secs() / 86_400).unwrap_or(0)
}

pub fn material_from_seed(
    role: WalletRole,
    env_var: String,
    seed: CipherSeed,
) -> anyhow::Result<SeedMaterial> {
    let birthday = seed.birthday();
    let seed_words = seed_words_from_seed(&seed)?;
    let wallet = WalletType::SeedWords(
        SeedWordsWallet::construct_new(seed).map_err(|e| anyhow::anyhow!("{e}"))?,
    );
    let key_manager = KeyManager::new(wallet.clone()).map_err(|e| anyhow::anyhow!("{e}"))?;
    let private_view_key = key_manager.get_private_view_key();
    let wallet_fingerprint_hex = wallet_fingerprint_hex(&wallet);
    let address = TariAddress::new_dual_address(
        wallet.get_public_view_key(),
        wallet.get_public_spend_key(),
        Network::Esmeralda,
        TariAddressFeatures::create_one_sided_only(),
        None,
    )?;

    Ok(SeedMaterial {
        role,
        env_var,
        address: address.to_base58(),
        birthday,
        seed_words,
        private_view_key_hex: hex::encode(private_view_key.as_bytes()),
        public_spend_key_hex: wallet.get_public_spend_key().to_hex(),
        wallet_fingerprint_hex,
    })
}

fn wallet_fingerprint_hex(wallet: &WalletType) -> String {
    let public_spend = wallet.get_public_spend_key();
    let public_view = wallet.get_public_view_key();
    let fingerprint = DomainSeparatedHasher::<KeyDigest, KeyManagerDomain>::new_with_label(
        WALLET_FINGERPRINT_LABEL,
    )
    .chain(public_spend.as_bytes())
    .chain(public_view.as_bytes())
    .chain(0_u64.to_le_bytes())
    .finalize();
    hex::encode(fingerprint.as_ref())
}

pub fn derive_distinct_recipient_pool(count: u32) -> anyhow::Result<Vec<String>> {
    (0..count).map(deterministic_recipient_address).collect()
}

fn deterministic_recipient_address(index: u32) -> anyhow::Result<String> {
    let view_key = deterministic_public_key("wallet-bench-s5-view", index)?;
    let spend_key = deterministic_public_key("wallet-bench-s5-spend", index)?;
    Ok(TariAddress::new_dual_address(
        view_key,
        spend_key,
        Network::Esmeralda,
        TariAddressFeatures::create_one_sided_only(),
        None,
    )?
    .to_base58())
}

fn deterministic_public_key(label: &str, index: u32) -> anyhow::Result<CompressedPublicKey> {
    let mut wide = [0u8; 64];
    let first = sha2::Sha512::digest(format!("{label}:{index}:0").as_bytes());
    let second = sha2::Sha512::digest(format!("{label}:{index}:1").as_bytes());
    wide[..32].copy_from_slice(&first[..32]);
    wide[32..].copy_from_slice(&second[..32]);
    let secret = RistrettoSecretKey::from_uniform_bytes(&wide)
        .map_err(|error| anyhow::anyhow!("deterministic key derivation failed: {error}"))?;
    if secret.as_bytes().iter().all(|byte| *byte == 0) {
        bail!("deterministic key derivation produced zero key");
    }
    Ok(CompressedPublicKey::from_secret_key(&secret))
}

#[cfg(test)]
mod tests {
    use super::*;

    fn unique_seed_config(prefix: &str) -> Config {
        let mut config = Config::default();
        config.seeds.old_wallet_env = format!("{prefix}_OLD");
        config.seeds.new_wallet_env = format!("{prefix}_NEW");
        config.seeds.payment_processor_env = format!("{prefix}_PP");
        config
    }

    #[test]
    fn generate_fresh_ignores_exported_seed_words() {
        let config = unique_seed_config("WALLET_BENCH_TEST_GENERATE_FRESH");
        let exported = seed_words_from_seed(&CipherSeed::random()).unwrap();
        for (_, env_var) in seed_envs(&config.seeds) {
            // SAFETY: these test-specific names are not read by other code.
            unsafe { env::set_var(&env_var, &exported) };
        }

        let book = AddressBook::generate_fresh(&config).unwrap();

        for (_, env_var) in seed_envs(&config.seeds) {
            // SAFETY: restore the test-specific process environment immediately.
            unsafe { env::remove_var(env_var) };
        }
        assert!(
            book.addresses
                .values()
                .all(|material| material.seed_words != exported)
        );
    }

    #[test]
    fn load_required_rejects_missing_seed_environment() {
        let config = unique_seed_config("WALLET_BENCH_TEST_LOAD_REQUIRED_MISSING");
        for (_, env_var) in seed_envs(&config.seeds) {
            // SAFETY: these test-specific names are not read by other code.
            unsafe { env::remove_var(env_var) };
        }

        let error = AddressBook::load_required(&config).unwrap_err().to_string();
        assert!(error.contains("WALLET_BENCH_TEST_LOAD_REQUIRED_MISSING_OLD"));
        assert!(error.contains("must be set"));
    }

    #[test]
    fn generated_material_redacts_seed_words() {
        let mut seed = CipherSeed::random();
        seed.change_birthday(0);
        let material =
            material_from_seed(WalletRole::NewWallet, "HARNESS_SEED_NEW".to_string(), seed)
                .unwrap();
        assert!(material.address.starts_with('f'));
        assert_eq!(material.birthday, 0);
        let serialized = serde_json::to_string(&material).unwrap();
        assert!(!serialized.contains(&material.seed_words));
        assert!(!serialized.contains(&material.private_view_key_hex));
    }

    #[test]
    fn changing_birthday_preserves_address() {
        let seed = CipherSeed::random();
        let original = material_from_seed(
            WalletRole::NewWallet,
            "HARNESS_SEED_NEW".to_string(),
            seed.clone(),
        )
        .unwrap();
        let mut genesis = seed;
        genesis.change_birthday(0);
        let changed = material_from_seed(
            WalletRole::NewWallet,
            "HARNESS_SEED_NEW".to_string(),
            genesis,
        )
        .unwrap();

        assert_eq!(changed.birthday, 0);
        assert_eq!(changed.address, original.address);
    }

    #[test]
    fn seed_words_with_changed_birthday_preserve_address() {
        let seed = CipherSeed::random();
        let original =
            material_from_seed(WalletRole::OldWallet, "HARNESS_SEED_OLD".to_string(), seed)
                .unwrap();
        let rewritten_words = seed_words_with_birthday(&original.seed_words, 0).unwrap();
        let rewritten = material_from_seed(
            WalletRole::OldWallet,
            "HARNESS_SEED_OLD".to_string(),
            seed_from_words(&rewritten_words).unwrap(),
        )
        .unwrap();

        assert_eq!(rewritten.birthday, 0);
        assert_eq!(rewritten.address, original.address);
        assert_eq!(
            rewritten.private_view_key_hex,
            original.private_view_key_hex
        );
        assert_eq!(
            rewritten.public_spend_key_hex,
            original.public_spend_key_hex
        );
    }

    #[test]
    fn distinct_recipient_pool_is_deterministic_and_esmeralda() {
        let first = derive_distinct_recipient_pool(4).unwrap();
        let second = derive_distinct_recipient_pool(4).unwrap();
        assert_eq!(first, second);
        assert_eq!(first.len(), 4);
        assert_ne!(first[0], first[1]);
        assert!(first.iter().all(|address| address.starts_with('f')));
        for address in first {
            TariAddress::from_str(&address).unwrap();
        }
    }
}
