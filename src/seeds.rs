#[derive(Debug, Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum WalletRole {
    OldWallet,
    NewWallet,
    PaymentProcessor,
}

impl WalletRole {
    pub fn label(self) -> &'static str {
        match self {
            Self::OldWallet => "old_wallet",
            Self::NewWallet => "new_wallet",
            Self::PaymentProcessor => "payment_processor",
        }
    }
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct SeedMaterial {
    pub role: WalletRole,
    pub env_var: String,
    pub address: String,
    pub birthday: u16,
    #[serde(skip_serializing)]
    pub seed_words: String,
    pub private_view_key_hex: String,
    pub public_spend_key_hex: String,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct AddressBook {
    pub generated_at: chrono::DateTime<chrono::Utc>,
    pub addresses: BTreeMap<String, SeedMaterial>,
}

impl AddressBook {
    pub fn from_config_or_generate(config: &Config) -> anyhow::Result<Self> {
        let mut addresses = BTreeMap::new();
        for (role, env_var) in seed_envs(&config.seeds) {
            let seed = match env::var(&env_var) {
                Ok(words) => {
                    seed_from_words(&words).with_context(|| format!("parsing ${env_var}"))?
                }
                Err(_) => {
                    let mut seed = CipherSeed::random();
                    if matches!(role, WalletRole::OldWallet | WalletRole::PaymentProcessor) {
                        seed.change_birthday(0);
                    }
                    seed
                }
            };
            let material = material_from_seed(role, env_var, seed)?;
            addresses.insert(role.label().to_string(), material);
        }
        Ok(Self {
            generated_at: chrono::Utc::now(),
            addresses,
        })
    }

    pub fn write_env_file(&self, path: &Path) -> anyhow::Result<()> {
        if let Some(parent) = path.parent() {
            fs::create_dir_all(parent)?;
        }
        let mut file =
            tempfile::NamedTempFile::new_in(path.parent().unwrap_or_else(|| Path::new(".")))?;
        writeln!(file, "# Generated by wallet-bench. Keep this file secret.")?;
        for material in self.addresses.values() {
            writeln!(
                file,
                "export {}='{}'",
                material.env_var, material.seed_words
            )?;
        }
        file.persist(path)?;
        Ok(())
    }

    pub fn public_summary(&self) -> serde_json::Value {
        let entries = self
            .addresses
            .iter()
            .map(|(role, material)| {
                (
                    role.clone(),
                    serde_json::json!({
                        "env_var": material.env_var,
                        "address": material.address,
                        "birthday": material.birthday,
                        "private_view_key_hex": redact_middle(&material.private_view_key_hex),
                        "public_spend_key_hex": material.public_spend_key_hex,
                    }),
                )
            })
            .collect::<serde_json::Map<_, _>>();
        serde_json::Value::Object(entries)
    }
}

fn seed_envs(seeds: &SeedConfig) -> [(WalletRole, String); 3] {
    [
        (WalletRole::OldWallet, seeds.old_wallet_env.clone()),
        (WalletRole::NewWallet, seeds.new_wallet_env.clone()),
        (
            WalletRole::PaymentProcessor,
            seeds.payment_processor_env.clone(),
        ),
    ]
}

pub fn seed_from_words(words: &str) -> anyhow::Result<CipherSeed> {
    let mnemonic =
        SeedWords::from_str(words).map_err(|e| anyhow::anyhow!("invalid seed words: {e}"))?;
    CipherSeed::from_mnemonic(&mnemonic, None)
        .map_err(|e| anyhow::anyhow!("invalid cipher seed: {e}"))
}

pub fn material_from_seed(
    role: WalletRole,
    env_var: String,
    seed: CipherSeed,
) -> anyhow::Result<SeedMaterial> {
    let birthday = seed.birthday();
    let seed_words = seed
        .to_mnemonic(MnemonicLanguage::English, None)
        .map_err(|e| anyhow::anyhow!("seed to mnemonic failed: {e}"))?
        .join(" ")
        .reveal()
        .to_string();
    let wallet = WalletType::SeedWords(
        SeedWordsWallet::construct_new(seed).map_err(|e| anyhow::anyhow!("{e}"))?,
    );
    let key_manager = KeyManager::new(wallet.clone()).map_err(|e| anyhow::anyhow!("{e}"))?;
    let private_view_key = key_manager.get_private_view_key();
    let address = TariAddress::new_dual_address(
        wallet.get_public_view_key(),
        wallet.get_public_spend_key(),
        Network::Esmeralda,
        TariAddressFeatures::create_one_sided_only(),
        None,
    )?;

    Ok(SeedMaterial {
        role,
        env_var,
        address: address.to_base58(),
        birthday,
        seed_words,
        private_view_key_hex: hex::encode(private_view_key.as_bytes()),
        public_spend_key_hex: wallet.get_public_spend_key().to_hex(),
    })
}

pub fn redact_middle(value: &str) -> String {
    if value.len() <= 12 {
        return "***".to_string();
    }
    format!("{}...{}", &value[..6], &value[value.len() - 6..])
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn generated_material_redacts_seed_words() {
        let mut seed = CipherSeed::random();
        seed.change_birthday(0);
        let material =
            material_from_seed(WalletRole::NewWallet, "HARNESS_SEED_NEW".to_string(), seed)
                .unwrap();
        assert!(material.address.starts_with('f'));
        assert_eq!(material.birthday, 0);
        assert_eq!(redact_middle(&material.private_view_key_hex).len(), 15);
    }
}
